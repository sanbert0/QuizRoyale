var combobox1 = document.querySelector("#nump1");
var input11 = document.querySelector("#resp11");
var input12 = document.querySelector("#resp12");
var input13 = document.querySelector("#resp13");
var input14 = document.querySelector("#resp14");

  combobox1.addEventListener("click", function() {
    var selectedOption = combobox1.options[combobox1.selectedIndex];
    switch(selectedOption.value){
        case "uno100":
            input11.style.visibility = 'visible';
            input12.style.visibility = 'hidden';
            input13.style.visibility = 'hidden';
            input14.style.visibility = 'hidden';
        break;
        case "uno200":
            input11.style.visibility = 'hidden';
            input12.style.visibility = 'visible';
            input13.style.visibility = 'hidden';
            input14.style.visibility = 'hidden';
        break;
        case "uno300":
            input11.style.visibility = 'hidden';
            input12.style.visibility = 'hidden';
            input13.style.visibility = 'visible';
            input14.style.visibility = 'hidden';
        break;
        case "uno400":
            input11.style.visibility = 'hidden';
            input12.style.visibility = 'hidden';
            input13.style.visibility = 'hidden';
            input14.style.visibility = 'visible';
        break;
    }
  });
